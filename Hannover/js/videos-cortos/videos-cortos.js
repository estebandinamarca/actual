// EDIFICIO
var myVideo=document.getElementById("#edificio");
function playEdificio() { 
	if (edificio.paused)
	edificio.play(); 
	else 
	edificio.pause(); 
}
function CambiarEstiloEdificio(id) {
	var elemento = document.getElementById(id);
	if (elemento.className == "play-video-corto img-edificio-play") {
	elemento.className = "play-video-corto img-edificio-pause";
	}
	else {
	elemento.className = "play-video-corto img-edificio-play";
	}
} 
